package Office_Hours.Practice_09_15_2020;

public class MathClass {


    public static void main(String[] args) {
        // 10^3=10*10*10
        double a=Math.pow(2, 6);
        System.out.println("a = " + a);
        System.out.println("Math.pow(10, 3) = " + Math.pow(10, 3));


        Math.abs(10); /// absolute value
        System.out.println("Math.sqrt(9) = " + Math.sqrt(9));

        System.out.println("Math.sin(30) = " + Math.sin(30));

        int c=10;
        int b=-10;
        System.out.println("Math.max(c,b) = " + Math.max(c, b));



   }
}
